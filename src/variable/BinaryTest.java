package variable;

public class BinaryTest {

    public static void main(String[] args) {

        int num = 10;
        // 2진수 0B
        // 3진수 012
        // 16진수 0XA
        int bNum = 0B1010;
        int oNum = 012;
        int xNum = 0XA;

        System.out.println(num);
        System.out.println(bNum);
        System.out.println(oNum);
        System.out.println(xNum);
    }
}
